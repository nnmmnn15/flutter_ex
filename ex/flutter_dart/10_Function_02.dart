int addition(int num1, int num2){
  return num1 + num2;
}
int subtraction(int num1, int num2){
  return num1 - num2;
}
int multiplication(int num1, int num2){
  return num1 * num2;
}
double division(int num1, int num2){
  return num1 / num2;
}
main(){

  print('-----------------');
  print(addition(1, 10));
  print(subtraction(1, 10));
  print(multiplication(1, 10));
  print(division(1, 10));
}